                            $this->params['tipo'],
                            $this->params['parametros_anteriores'],
                            TIPO_DESCRIPCION_HTML);
                        if ($descripcion_parametros_anteriores !== NULL)
                        {
                            $hay_parametros_accion = true;
                            $info .= "<i class='icon-info-sign color-azul'></i> ".
                                $this->idiomas->_("Parámetros anteriores").": ".$descripcion_parametros_anteriores;
                        }
                        $descripcion_resultado = AccionUsuario::dame_descripcion_parametros_accion(
                            $this->params['tipo'],
                            $this->params['resultado'],
                            TIPO_DESCRIPCION_HTML);
                        if ($descripcion_resultado != NULL)
                        {
                            $hay_parametros_accion = true;
                            $info .= "<i class='icon-info-sign color-azul'></i> ".
                                $this->idiomas->_("Resultado").": ".$descripcion_resultado;
                        }
                    }
                    catch (Exception $e)
                    {
                        $log = dame_log();
                        $log->warn("Error al formatear los parámetros y el resultado: ", $e);

                        if ($this->params['parametros'] != "")
                        {
                            $hay_parametros_accion = true;
                            $info .= "<i class='icon-question-sign color-gris-claro'></i> ".
                                $this->idiomas->_("Parámetros").": ".$this->params['parametros']."</br>";
                        }
                        if ($this->params['parametros_anteriores'] != "")
                        {
                            $hay_parametros_accion = true;
                            $info .= "<i class='icon-question-sign color-gris-claro'></i> ".
                                $this->idiomas->_("Parámetros anteriores").": ".$this->params['parametros_anteriores']."</br>";
                        }
                        if ($this->params['resultado'] != "")
                        {
                            $hay_parametros_accion = true;
                            $info .= "<i class='icon-question-sign color-gris-claro'></i> ".
                                $this->idiomas->_("Resultado").": ".$this->params['resultado']."</br>";
                        }
                    }
                }
                if ($hay_parametros_accion == false)
                {
                    $info .= "<i class='icon-info-sign color-azul'></i> ".
                        $this->idiomas->_("No hay parámetros");
                }
            }
            else
            {
                $info .= "<i class='icon-warning-sign color-rojo'></i> ".
                    $this->idiomas->_("Sin información disponible");
            }

            return ($info);

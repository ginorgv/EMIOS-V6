		// Devuelve la cabecera para la acción
        static function dame_cabecera_tabla($mostrar_red)
		{
            $idiomas = new Idiomas();

            $cabecera_tabla = array();
            array_push($cabecera_tabla, $idiomas->_("Fecha"));
            if ($mostrar_red == true)
            {
                array_push($cabecera_tabla, $idiomas->_("Red"));
            }
            array_push($cabecera_tabla, $idiomas->_("Usuario"));
            array_push($cabecera_tabla, $idiomas->_("Tipo"));
            array_push($cabecera_tabla, $idiomas->_("Objeto"));
            return ($cabecera_tabla);
        }


        // Devuelve la consulta para las acciones
        static function dame_consulta_acciones(
            $modulo,
            $cadena_fecha_hora_inicio_base_datos_utc,
            $cadena_fecha_hora_fin_base_datos_utc)
        {
            $bd_datos = BaseDatosDatos::dame_base_datos();

            $consulta = "
                SELECT *
                FROM acciones_usuario
                WHERE
                    (hora >= '".$bd_datos->_($cadena_fecha_hora_inicio_base_datos_utc)."')
                    AND (hora <= '".$bd_datos->_($cadena_fecha_hora_fin_base_datos_utc)."')";
            if ($modulo == MODULO_RED)
            {
                $consulta .= "
                    AND (red = '".$_SESSION["id_red"]."')";
            }
            $consulta .= "
                ORDER BY hora DESC, id DESC";
			return ($consulta);
        }


        // Devuelve la consulta para la exportación de acciones
        static function dame_consulta_exportacion_acciones(
            $modulo,

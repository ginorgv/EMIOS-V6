        // Devuelve la tabla de acciones
        static function dame_tabla_acciones(
            $modulo,
            $filtro = null,
            $cadena_fecha_hora_inicio_base_datos_utc = null,
            $cadena_fecha_hora_fin_base_datos_utc = null,
            &$limite_elementos_tabla_historico_superado = null)
		{
            $idiomas = new Idiomas();
            $bd_red = BaseDatosRed::dame_base_datos();
            $bd_datos = BaseDatosDatos::dame_base_datos();

            // Se crea la tabla
            switch ($modulo)
            {
                case MODULO_MONITORIZACION:
                {
                    $numero_columnas = NUMERO_COLUMNAS_TABLA_ACCIONES_USUARIO_CON_RED;
                    $anchuras_columnas = unserialize(ANCHURAS_COLUMNAS_TABLA_ACCIONES_USUARIO_CON_RED);
                    $mostrar_red = true;
                    break;
                }
                default:
                {
                    $numero_columnas = NUMERO_COLUMNAS_TABLA_ACCIONES_USUARIO_SIN_RED;
                    $anchuras_columnas = unserialize(ANCHURAS_COLUMNAS_TABLA_ACCIONES_USUARIO_SIN_RED);
                    $mostrar_red = false;
                    break;
                }
            }
            $params_tabla = array(
                "numero_columnas" => $numero_columnas,
                "anchuras_columnas" => $anchuras_columnas,
                "tipo_fila" => TIPO_FILA_TABLA_DATOS_DETALLES,
                "generar_valores_xml" => true
            );
            $tabla = new TablaDatos(
                "tabla-acciones-usuario",
                $idiomas->_("Acciones"),
                TIPO_TABLA_DATOS_LISTA,
                $params_tabla
            );
            $cabecera = AccionUsuario::dame_cabecera_tabla($mostrar_red);
            $tabla->anyade_cabecera("", $cabecera);

            // Se añade cada una de las acciones a la tabla y el pie de tabla
            // (si no hay fechas, se devuelve la tabla vacía)
            $numero_acciones = 0;
            if (($cadena_fecha_hora_inicio_base_datos_utc !== NULL) && ($cadena_fecha_hora_fin_base_datos_utc !== NULL))
            {
                // Se realiza la consulta de las acciones
                $consulta_acciones = AccionUsuario::dame_consulta_acciones(
                    $modulo,
                    $cadena_fecha_hora_inicio_base_datos_utc,
                    $cadena_fecha_hora_fin_base_datos_utc);
                $res_acciones = $bd_datos->ejecuta_consulta($consulta_acciones);
                if ($res_acciones == false)
                {
                    throw new Exception("Error en la consulta: '".$consulta_acciones."'");
                }

                // Filas de las acciones
                $filas_acciones = array();
                while ($fila_accion = $res_acciones->dame_siguiente_fila())
                {
                    array_push($filas_acciones, $fila_accion);
                }

                // Si el módulo de monitorización, se muestra el nombre de la red
                // (se añaden los nombres de las redes a los datos de las filas de las acciones
                if ($modulo == MODULO_MONITORIZACION)
                {
                    $consulta_redes = "
                        SELECT
                            id,
                            nombre
                        FROM redes";
                    $res_redes = $bd_red->ejecuta_consulta($consulta_redes);
                    $nombres_redes = array();
                    while ($fila_red = $res_redes->dame_siguiente_fila())
                    {
                        $nombres_redes[$fila_red["id"]] = $fila_red["nombre"];
                    }
                    for ($i = 0; $i < count($filas_acciones); $i++)
                    {
                        $id_red = $filas_acciones[$i]["red"];
                        if ($id_red == ID_NINGUNO)
                        {
                            $nombre_red = $idiomas->_("Ninguna");
                        }
                        else
                        {
                            $nombre_red = $nombres_redes[$id_red];
                        }
                        $filas_acciones[$i]["nombre_red"] = $nombre_red;
                    }
                }

                // Se recorren las filas de las acciones y se añaden las que cumplan con el filtro
                $limite_elementos_tabla_historico_superado = false;
                foreach ($filas_acciones as $fila_accion)
                {
                    $accion = new AccionUsuario($fila_accion);
                    $datos_tabla = $accion->dame_datos_tabla($mostrar_red);

                    // Se realiza el filtrado
                    // (no se hace en la consulta porque se filtra por el nombre de la red y la descripción del tipo)
                    if ($mostrar_red == true)
                    {
                        $nombre_red = $datos_tabla[1];
                        $id_usuario = $datos_tabla[2];
                        $descripcion_tipo = $datos_tabla[3];
                        $descripcion_objeto = $datos_tabla[4];
                        if (($filtro != "") &&
                            (stripos($nombre_red, $filtro) === false) &&
                            (stripos($id_usuario, $filtro) === false) &&
                            (stripos($descripcion_tipo, $filtro) === false) &&
                            (stripos($descripcion_objeto, $filtro) === false))
                        {
                            continue;
                        }
                    }
                    else
                    {
                        $id_usuario = $datos_tabla[1];
                        $descripcion_tipo = $datos_tabla[2];
                        $descripcion_objeto = $datos_tabla[3];
                        if (($filtro != "") &&
                            (stripos($id_usuario, $filtro) === false) &&
                            (stripos($descripcion_tipo, $filtro) === false) &&
                            (stripos($descripcion_objeto, $filtro) === false))
                        {
                            continue;
                        }
                    }

                    if ($numero_acciones == NUMERO_MAXIMO_ELEMENTOS_TABLAS_HISTORICOS)
                    {
                        $limite_elementos_tabla_historico_superado = true;
                        break;
                    }
                    else
                    {
                        $tabla->anyade_fila(
                            "datosAccionUsuario__".$fila_accion['id'],
                            $datos_tabla
                        );
                        $numero_acciones += 1;
                    }
                }
            }
            $texto_pie = $idiomas->_("Número de acciones").": ".$numero_acciones;
            if ($limite_elementos_tabla_historico_superado == true)
            {
                $texto_pie .= " (".$idiomas->_("límite máximo superado").")";
            }
            $tabla->anyade_pie($texto_pie);

            return ($tabla->dame_tabla());
		}


        // Exporta las acciones
        static function exporta_acciones(
            $modulo,
            $filtro,
            $cadena_fecha_hora_inicio_base_datos_utc,
            $cadena_fecha_hora_fin_base_datos_utc,
            &$ruta_relativa_fichero_acciones_exportadas)
        {
            $bd_datos = BaseDatosDatos::dame_base_datos();

            // Mostrar la red
            switch ($modulo)
            {
                case MODULO_MONITORIZACION:
                {
                    $mostrar_red = true;
                    break;
                }
                default:
                {
                    $mostrar_red = false;
                    break;
                }
            }

            // Se exportan las acciones a un fichero CSV
            $numero_acciones = 0;
            if (($cadena_fecha_hora_inicio_base_datos_utc !== NULL) && ($cadena_fecha_hora_fin_base_datos_utc !== NULL))
            {
                $consulta = AccionUsuario::dame_consulta_exportacion_acciones($modulo, $cadena_fecha_hora_inicio_base_datos_utc, $cadena_fecha_hora_fin_base_datos_utc);
                $res = $bd_datos->ejecuta_consulta($consulta);
                if ($res == false)
                {
                    throw new Exception("Error en la consulta: '".$consulta."'");
                }
                $filas_acciones_csv = array();
                while ($fila = $res->dame_siguiente_fila())
                {
                    $accion = new AccionUsuario($fila);
                    $datos_tabla = $accion->dame_datos_tabla($mostrar_red);

                    // Se realiza el filtrado
                    // (no se hace en la consulta porque se filtra por el nombre de la red y la descripción del tipo)
                    if ($mostrar_red == true)
                    {
                        $nombre_red = $datos_tabla[1];
                        $id_usuario = $datos_tabla[2];
                        $descripcion_tipo = $datos_tabla[3];
                        $descripcion_objeto = $datos_tabla[4];
                        if (($filtro != "") &&
                            (stripos($nombre_red, $filtro) === false) &&
                            (stripos($id_usuario, $filtro) === false) &&
                            (stripos($descripcion_tipo, $filtro) === false) &&
                            (stripos($descripcion_objeto, $filtro) === false))
                        {
                            continue;
                        }
                    }
                    else
                    {
                        $id_usuario = $datos_tabla[1];
                        $descripcion_tipo = $datos_tabla[2];
                        $descripcion_objeto = $datos_tabla[3];
                        if (($filtro != "") &&
                            (stripos($id_usuario, $filtro) === false) &&
                            (stripos($descripcion_tipo, $filtro) === false) &&
                            (stripos($descripcion_objeto, $filtro) === false))
                        {
                            continue;
                        }
                    }

                    // Campos de la fila
                    $hora = $fila["hora"];
                    $id_usuario = $fila["usuario"];
                    $tipo = $fila["tipo"];
                    $objeto = $fila["objeto"];
                    $parametros = $fila["parametros"];
                    $parametros_anteriores = $fila["parametros_anteriores"];
                    $resultado = $fila["resultado"];
                    $observaciones = $fila["observaciones"];

                    // Límite de histórico de acciones
                    if ($numero_acciones == NUMERO_MAXIMO_ELEMENTOS_TABLAS_HISTORICOS)
                    {
                        break;
                    }

                    // Tipo y objeto
                    $parametros_objeto = explode(SEPARADOR_PARAMETROS_SIMPLES, $objeto);
                    if (count($parametros_objeto) == 1)
                    {
                        $descripcion_objeto = $parametros_objeto[0];
                    }
                    else
                    {
                        $descripcion_objeto = $parametros_objeto[1]." -> ".$parametros_objeto[0];
                    }

                    // Parámetros de la acción
                    if (($_SESSION["perfil"] != PERFIL_USUARIO_ESTANDAR) || (strtolower($fila["usuario"]) == $_SESSION["id_usuario"]))
                    {
                        $descripcion_parametros = AccionUsuario::dame_descripcion_parametros_accion($tipo, $parametros, TIPO_DESCRIPCION_TEXTO);
                        $descripcion_parametros_anteriores = AccionUsuario::dame_descripcion_parametros_accion($tipo, $parametros_anteriores, TIPO_DESCRIPCION_TEXTO);
                        $descripcion_resultado = AccionUsuario::dame_descripcion_parametros_accion($tipo, $resultado, TIPO_DESCRIPCION_TEXTO);
                        if ($descripcion_parametros == NULL)
                        {
                            $descripcion_parametros = "";
                        }
                        else
                        {
                            $descripcion_parametros = '"'.$descripcion_parametros.'"';
                        }
                        if ($descripcion_parametros_anteriores == NULL)
                        {
                            $descripcion_parametros_anteriores = "";
                        }
                        else
                        {
                            $descripcion_parametros_anteriores = '"'.$descripcion_parametros_anteriores.'"';
                        }
                        if ($descripcion_resultado == NULL)
                        {
                            $descripcion_resultado = "";
                        }
                        else
                        {
                            $descripcion_resultado = '"'.$descripcion_resultado.'"';
                        }
                    }
                    else
                    {
                        $descripcion_parametros = "";
                        $descripcion_parametros_anteriores = "";
                        $descripcion_resultado = "";
                    }

                    // Se añade la fila CSV
                    $fila_csv = array();
                    $fila_csv["hora"] = $hora;
                    if ($mostrar_red == true)
                    {
                        $fila_csv["red"] = $nombre_red;
                    }
                    $fila_csv["usuario"] = $id_usuario;
                    $fila_csv["tipo"] = $descripcion_tipo;
                    $fila_csv["objeto"] = $descripcion_objeto;
                    $fila_csv["parametros"] = $descripcion_parametros;
                    $fila_csv["parametros_anteriores"] = $descripcion_parametros_anteriores;
                    $fila_csv["resultado"] = $descripcion_resultado;
                    $fila_csv["observaciones"] = $observaciones;
                    array_push($filas_acciones_csv, $fila_csv);

                    $numero_acciones += 1;
                }
                $numero_acciones_exportadas = AccionUsuario::crea_fichero_csv_acciones(
                    $mostrar_red,
                    $filas_acciones_csv,
                    $ruta_relativa_fichero_acciones_exportadas);
            }
            return ($numero_acciones_exportadas);
		}



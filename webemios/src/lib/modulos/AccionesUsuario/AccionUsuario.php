        // Crea un fichero CSV con los parámetros argumento
        static function crea_fichero_csv_acciones(
            $mostrar_red,
            $filas_acciones_csv,
            &$ruta_relativa_fichero_acciones_exportadas)
        {
            // Pasos de creación de un fichero CSV de valores exportados de un sensor:
            // - Conversión de fechas UTC a zona horaria actual
            // - Se crea el nombre del fichero
            // - Se crean las rutas tanto de cliente como de servidor
            // - Se crean las cabeceras del fichero CSV
            // - Se escribe el fichero CSV

            $zona_horaria = dame_zona_horaria_local();
            for ($i = 0; $i < count($filas_acciones_csv); $i++)
            {
                $cadena_fecha_hora_utc = convierte_formato_fecha($filas_acciones_csv[$i]["hora"], FORMATO_FECHA_HORA_BASE_DATOS, $_SESSION["formato_fecha_hora_local"]);
                $cadena_fecha_hora_local = cambia_zona_horaria_cadena_fecha_hora($cadena_fecha_hora_utc, $_SESSION["formato_fecha_hora_local"], ZONA_HORARIA_UTC, $zona_horaria);
                $filas_acciones_csv[$i]["hora"] = $cadena_fecha_hora_local;
            }

            $idiomas = new Idiomas();

            // Directorio de ficheros temporales del usuario
            $directorio_absoluto_ficheros_temporales_usuario = dame_directorio_ficheros_temporales_usuario($_SESSION["id_usuario"]);
            $directorio_relativo_ficheros_temporales_usuario = str_replace($_SESSION["directorio"], ".", $directorio_absoluto_ficheros_temporales_usuario);

            $numero_acciones_exportadas = count($filas_acciones_csv);
            if ($numero_acciones_exportadas > 0)
            {
                if ($mostrar_red == false)
                {
                    $nombre_red = dame_nombre_red($_SESSION["id_red"]);
                }
                $cadena_fecha_hora_inicio_acciones_fichero = convierte_formato_fecha($filas_acciones_csv[0]["hora"], $_SESSION["formato_fecha_hora_local"], FORMATO_FECHA_HORA_FICHERO);
                $cadena_fecha_hora_fin_acciones_fichero = convierte_formato_fecha($filas_acciones_csv[$numero_acciones_exportadas - 1]["hora"], $_SESSION["formato_fecha_hora_local"], FORMATO_FECHA_HORA_FICHERO);
                $nombre_fichero_acciones_exportadas = $idiomas->_("acciones");
                if ($mostrar_red == false)
                {
                    $nombre_fichero_acciones_exportadas .= "-".$idiomas->_("red")."-".$nombre_red;
                }
                $nombre_fichero_acciones_exportadas .= "-".$cadena_fecha_hora_inicio_acciones_fichero."-".$cadena_fecha_hora_fin_acciones_fichero.".csv";

                $ruta_absoluta_fichero_acciones_exportadas = $directorio_absoluto_ficheros_temporales_usuario.'/'.$nombre_fichero_acciones_exportadas;
                $ruta_relativa_fichero_acciones_exportadas = $directorio_relativo_ficheros_temporales_usuario.'/'.$nombre_fichero_acciones_exportadas;

                $cabecera_fichero_acciones = array();
                array_push($cabecera_fichero_acciones, $idiomas->_("Fecha"));
                if ($mostrar_red == true)
                {
                    array_push($cabecera_fichero_acciones, $idiomas->_("Red"));
                }
                array_push($cabecera_fichero_acciones, $idiomas->_("Usuario"));
                array_push($cabecera_fichero_acciones, $idiomas->_("Tipo"));
                array_push($cabecera_fichero_acciones, $idiomas->_("Objeto"));
                array_push($cabecera_fichero_acciones, $idiomas->_("Parámetros"));
                array_push($cabecera_fichero_acciones, $idiomas->_("Parámetros anteriores"));
                array_push($cabecera_fichero_acciones, $idiomas->_("Resultado"));
                array_push($cabecera_fichero_acciones, $idiomas->_("Observaciones"));
                array_unshift($filas_acciones_csv, $cabecera_fichero_acciones);

                escribe_fichero_valores_csv($ruta_absoluta_fichero_acciones_exportadas, $filas_acciones_csv);
            }

            return ($numero_acciones_exportadas);
        }

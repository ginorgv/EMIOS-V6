        // Devuelve la descripcion de los parámetros de la acción
        static function dame_descripcion_parametros_accion($tipo_accion, $parametros_accion_json, $tipo_descripcion)
        {
            if ($parametros_accion_json == "")
            {
                $descripcion_parametros_accion = NULL;
            }
            else
            {
                // Delimitadores de descripción de parámetros
                $cadena_inicio_lista_parametros = "";
                $cadena_fin_lista_parametros = "";
                $cadena_inicio_primer_parametro = "";
                $cadena_inicio_parametro = "";
                $cadena_fin_parametro = "";
                establece_delimitadores_descripcion_parametros(
                    $tipo_descripcion,
                    $cadena_inicio_lista_parametros,
                    $cadena_fin_lista_parametros,
                    $cadena_inicio_primer_parametro,
                    $cadena_inicio_parametro,
                    $cadena_fin_parametro);

                // Descripción de parámetros
                $parametros_accion = json_decode($parametros_accion_json, true);
                $descripcion_parametros_accion = $cadena_inicio_lista_parametros;
                $numero_parametros_mostrados = 0;
                foreach ($parametros_accion as $parametro_accion => $valor_parametro_accion)
                {
                    $mostrar_parametro_accion = AccionUsuario::dame_mostrar_parametro_accion(
                        $tipo_accion,
                        $parametro_accion,
                        $valor_parametro_accion);
                    if ($mostrar_parametro_accion == true)
                    {
                        $descripcion_parametro_accion = AccionUsuario::dame_descripcion_parametro_accion($parametro_accion);
                        $descripcion_valor_parametro_accion = AccionUsuario::dame_descripcion_valor_parametro_accion(
                            $parametro_accion,
                            $valor_parametro_accion,
                            $parametros_accion,
                            $tipo_descripcion);
                        if ($descripcion_valor_parametro_accion == "")
                        {
                            continue;
                        }
                        if ($numero_parametros_mostrados == 0)
                        {
                            $descripcion_parametros_accion .= $cadena_inicio_primer_parametro;
                        }
                        else
                        {
                            $descripcion_parametros_accion .= $cadena_inicio_parametro;
                        }
                        $descripcion_parametros_accion .= $descripcion_parametro_accion.": ".$descripcion_valor_parametro_accion.$cadena_fin_parametro;
                        $numero_parametros_mostrados += 1;
                    }
                }
                $descripcion_parametros_accion .= $cadena_fin_lista_parametros;
                if ($numero_parametros_mostrados == 0)
                {
                    $descripcion_parametros_accion = NULL;
                }
            }
            return ($descripcion_parametros_accion);
        }


        // Devuelve si hay que mostrar el parámetro de la acción
        static function dame_mostrar_parametro_accion(
            $tipo_accion,
            $parametro_accion,
            $valor_parametro_accion)
        {
            $mostrar_parametro_accion = true;
            switch ($tipo_accion)
            {
                // Varios módulos
                case TIPO_ACCION_USUARIO_GUARDAR_IMAGEN_BASE_DATOS:
                case TIPO_ACCION_USUARIO_DUPLICAR_IMAGEN_BASE_DATOS:
                {
                    switch ($parametro_accion)
                    {
                        case PARAMETRO_ACCION_USUARIO_ORIGEN_IMAGEN:
                        {
                            $mostrar_parametro_accion = false;
                            break;
                        }
                    }
                    break;
                }
                case TIPO_ACCION_USUARIO_ANYADIR_PERIODO:
                case TIPO_ACCION_USUARIO_MODIFICAR_PERIODO:
                case TIPO_ACCION_USUARIO_ELIMINAR_PERIODO:
                {
                    switch ($parametro_accion)
                    {
                        case PARAMETRO_ACCION_USUARIO_ORIGEN_PERIODO:
                        {
                            $mostrar_parametro_accion = false;
                            break;
                        }
                    }
                    break;
                }
                case TIPO_ACCION_USUARIO_ANYADIR_RANGO_DIAS:
                case TIPO_ACCION_USUARIO_MODIFICAR_RANGO_DIAS:
                case TIPO_ACCION_USUARIO_ELIMINAR_RANGO_DIAS:
                {
                    switch ($parametro_accion)
                    {
                        case PARAMETRO_ACCION_USUARIO_ORIGEN_RANGO_DIAS:
                        {
                            $mostrar_parametro_accion = false;
                            break;
                        }
                    }
                    break;
                }
                // Módulo Administración
                case TIPO_ACCION_USUARIO_INICIO_SESION:
                {
                    switch ($parametro_accion)
                    {
                        case PARAMETRO_ACCION_USUARIO_DIRECCION_IP:
                        {
                            if ($_SESSION["perfil"] != PERFIL_USUARIO_SUPERADMINISTRADOR)
                            {
                                $mostrar_parametro_accion = false;
                            }
                            break;
                        }
                    }
                }
                // Módulo Sensores
                case TIPO_ACCION_USUARIO_BORRAR_VALORES_SENSOR:
                {
                    switch ($parametro_accion)
                    {
                        case PARAMETRO_ACCION_USUARIO_FECHA_HORA_INICIO:
                        case PARAMETRO_ACCION_USUARIO_FECHA_HORA_FIN:
                        {
                            if ($valor_parametro_accion == "")
                            {
                                $mostrar_parametro_accion = false;
                            }
                            break;
                        }
                    }
                }
                // Módulo SmartMeter
                case TIPO_ACCION_USUARIO_GUARDAR_FECHA_RECALCULO_DATOS:
                case TIPO_ACCION_USUARIO_ASIGNA_TARIFA_GRUPO_TARIFAS_SENSORES:
                case TIPO_ACCION_USUARIO_MODIFICA_TARIFAS:
                case TIPO_ACCION_USUARIO_ANYADIR_TARIFA:
                case TIPO_ACCION_USUARIO_MODIFICAR_TARIFA:
                case TIPO_ACCION_USUARIO_ELIMINAR_TARIFA:
                case TIPO_ACCION_USUARIO_ANYADIR_GRUPO_TARIFAS:
                case TIPO_ACCION_USUARIO_MODIFICAR_GRUPO_TARIFAS:
                case TIPO_ACCION_USUARIO_ELIMINAR_GRUPO_TARIFAS:
                case TIPO_ACCION_USUARIO_ANYADIR_CONCEPTO_ADICIONAL_FACTURA_TARIFA:
                case TIPO_ACCION_USUARIO_MODIFICAR_CONCEPTO_ADICIONAL_FACTURA_TARIFA:
                case TIPO_ACCION_USUARIO_ELIMINAR_CONCEPTO_ADICIONAL_FACTURA_TARIFA:
                {
                    switch ($parametro_accion)
                    {
                        case PARAMETRO_ACCION_USUARIO_MEDICION:
                        {
                            $mostrar_parametro_accion = false;
                            break;
                        }
                    }
                    break;
                }
            }
            return ($mostrar_parametro_accion);
        }



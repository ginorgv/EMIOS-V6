            if (($_SESSION["perfil"] != PERFIL_USUARIO_ESTANDAR) || (strtolower($this->params['usuario']) == $_SESSION["id_usuario"]))
            {
                // Parámetros, parámetros anteriores y resultado (si los hay)
                if (($this->params['parametros'] == "") &&
                    ($this->params['parametros_anteriores'] == "") &&
                    ($this->params['resultado'] == ""))
                {
                    $hay_parametros_accion = false;
                }
                else
                {
                    try
                    {
                        $descripcion_parametros = AccionUsuario::dame_descripcion_parametros_accion(
                            $this->params['tipo'],
                            $this->params['parametros'],
                            TIPO_DESCRIPCION_HTML);
                        if ($descripcion_parametros !== NULL)
                        {
                            $hay_parametros_accion = true;
                            $info .= "<i class='icon-info-sign color-azul'></i> ".
                                $this->idiomas->_("Parámetros").": ".$descripcion_parametros;
                        }

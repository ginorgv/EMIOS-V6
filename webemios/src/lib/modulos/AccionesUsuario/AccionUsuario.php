		function dame_datos_tabla($mostrar_red)
		{
            // Iconos de datos erróneos
            $icono_fila_con_errores = dame_icono_fila_con_errores();
            $icono_dato_erroneo = dame_icono_dato_erroneo();

            // Inicialización de datos de la tabla
            $cadena_fecha_hora_local_local = $icono_dato_erroneo;
            $nombre_red = $icono_dato_erroneo;
            $id_usuario = $icono_dato_erroneo;
            $descripcion_tipo = $icono_dato_erroneo;
            $descripcion_objeto = $icono_dato_erroneo;

            // Se recuperan los datos de la tabla
            $fecha_hora_correcta = false;
            try
            {
                // Conversión de fechas
                $zona_horaria = dame_zona_horaria_local();
                $cadena_fecha_hora_local_utc = convierte_formato_fecha($this->params['hora'], FORMATO_FECHA_HORA_BASE_DATOS, $_SESSION["formato_fecha_hora_local"]);
                $cadena_fecha_hora_local_local = cambia_zona_horaria_cadena_fecha_hora($cadena_fecha_hora_local_utc, $_SESSION["formato_fecha_hora_local"], ZONA_HORARIA_UTC, $zona_horaria);
                $fecha_hora_correcta = true;

                // Red
                if ($mostrar_red == true)
                {
                    $nombre_red = htmlspecialchars($this->params["nombre_red"], ENT_QUOTES);
                }

                // Usuario, tipo y objeto (se tiene en cuenta el cambio de nombre en el objeto)
                $id_usuario = htmlspecialchars($this->params['usuario'], ENT_QUOTES);
                $descripcion_tipo = AccionUsuario::dame_descripcion_tipo_accion($this->params['tipo'], $this->params['parametros']);
                $descripcion_tipo = htmlspecialchars($descripcion_tipo, ENT_QUOTES);
                $nombres_objeto = explode(SEPARADOR_PARAMETROS_SIMPLES, $this->params['objeto']);
                if (count($nombres_objeto) == 1)
                {
                    $nombre_objeto = $nombres_objeto[0];
                    if ($nombre_objeto == "")
                    {
                        $nombre_objeto = $this->idiomas->_("Ninguno");
                    }
                    $descripcion_objeto = htmlspecialchars($nombre_objeto, ENT_QUOTES);
                }
                else
                {
                    $nombre_objeto = $nombres_objeto[0];
                    $nombre_objeto_anterior = $nombres_objeto[1];
                    restaura_valor_parametro_nombre_elemento_accion_usuario($nombre_objeto);
                    restaura_valor_parametro_nombre_elemento_accion_usuario($nombre_objeto_anterior);

                    // Flag para mostrar el objeto anterior
                    $mostrar_objeto_anterior = true;

                    // Tipo de acción
                    switch ($this->params['tipo'])
                    {
                        case TIPO_ACCION_USUARIO_SELECCION_RED:
                        {
                            if ($_SESSION["perfil"] != PERFIL_USUARIO_SUPERADMINISTRADOR)
                            {
                                $mostrar_objeto_anterior = false;
                            }
                            break;
                        }
                    }

                    // Descripción del objeto
                    if ($mostrar_objeto_anterior == true)
                    {
                        $descripcion_objeto =
                            htmlspecialchars($nombre_objeto_anterior, ENT_QUOTES)." ".
                            "<i class='icon-arrow-right color-gris-muy-claro'>".
                                "<texto class='elemento-oculto'>".htmlspecialchars("->", ENT_QUOTES)."</texto></i>"." ".
                            htmlspecialchars($nombre_objeto, ENT_QUOTES);
                    }
                    else
                    {
                        $descripcion_objeto = htmlspecialchars($nombre_objeto, ENT_QUOTES);
                    }
                }
            }
            catch (Exception $e)
            {
                // Se añade información de la excepción en el log
                $log = dame_log();
                $log->error("[".$_SESSION["id_usuario"]."] "."Excepción capturada: ", $e);

                // Se añade icono de error en la fecha
                if ($fecha_hora_correcta == true)
                {
                    $cadena_fecha_hora_local_local = "[".$icono_fila_con_errores."] ".$cadena_fecha_hora_local_local;
                }
            }

            // Se devuelven los datos de la tabla
            $datos_tabla = array();
            array_push($datos_tabla, $cadena_fecha_hora_local_local);
            if ($mostrar_red == true)
            {
                array_push($datos_tabla, $nombre_red);
            }
            array_push($datos_tabla, $id_usuario);
            array_push($datos_tabla, $descripcion_tipo);
            array_push($datos_tabla, $descripcion_objeto);
            return ($datos_tabla);

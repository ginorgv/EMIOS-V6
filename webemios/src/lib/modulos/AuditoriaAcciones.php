                    $log->warn("[".$id_usuario."] Instalación no visible por el usuario actual (id: '".$id_instalacion_accion."')");
                    $parametros_accion_correctos = false;
                }
            }

            // Se comprueba el ratio
            if ($id_ratio_accion !== NULL)
            {
                $ids_ratios_usuario_actual = dame_ids_ratios_usuario_actual();
                if (in_array($id_ratio_accion, $ids_ratios_usuario_actual) == false)
                {
                    if ($id_ratio_accion !== NULL)
                    {
                        $log->warn("[".$id_usuario."] Ratio no visible por el usuario actual (id: '".$id_ratio_accion."')");
                        $parametros_accion_correctos = false;
                    }
                }
            }

            // Se comprueban los sensores
            if ($ids_sensores_accion !== NULL)
            {
                $ids_sensores_usuario_actual = dame_todos_ids_sensores_usuario_actual();
                foreach ($ids_sensores_accion as $id_sensor_accion)
                {
                    if ($id_sensor_accion !== NULL)
                    {
                        if (in_array($id_sensor_accion, $ids_sensores_usuario_actual) == false)
                        {
                            $log->warn("[".$id_usuario."] Sensor no visible por el usuario actual (id: '".$id_sensor_accion."')");
                            $parametros_accion_correctos = false;
                        }
                    }
                }
            }

            // Se comprueba el grupo de sensores
            if ($id_grupo_sensores_accion !== NULL)
            {
                $ids_grupos_sensores_usuario_actual = dame_todos_ids_grupos_sensores_usuario_actual();
                if (in_array($id_grupo_sensores_accion, $ids_grupos_sensores_usuario_actual) == false)
                {
                    $log->warn("[".$id_usuario."] Grupo de sensores no visible por el usuario actual (id: '".$id_grupo_sensores_accion."')");
                    $parametros_accion_correctos = false;
                }
            }

            // Se comprueba el evento
            if ($id_evento_accion !== NULL)
            {
                $ids_sensores_usuario_actual = dame_todos_ids_sensores_usuario_actual();
                $ids_grupos_sensores_usuario_actual = dame_todos_ids_grupos_sensores_usuario_actual();
                $ids_eventos_usuario_actual = Evento::dame_ids_eventos_usuario_actual(
                    $ids_sensores_usuario_actual,
                    $ids_grupos_sensores_usuario_actual);
                if (in_array($id_evento_accion, $ids_eventos_usuario_actual) == false)
                {
                    $log->warn("[".$id_usuario."] Evento no visible por el usuario actual (id: '".$id_evento_accion."')");
                    $parametros_accion_correctos = false;
                }
            }

            // Se comprueba el actuador
            if ($id_actuador_accion !== NULL)
            {
                $ids_actuadores_usuario_actual = dame_todos_ids_actuadores_usuario_actual();
                if (in_array($id_actuador_accion, $ids_actuadores_usuario_actual) == false)
                {
                    $log->warn("[".$id_usuario."] Actuador no visible por el usuario actual (id: '".$id_actuador_accion."')");
                    $parametros_accion_correctos = false;
                }
            }

            // Se comprueba el grupo de actuadores
            if ($id_grupo_actuadores_accion !== NULL)
            {
                $ids_grupos_actuadores_usuario_actual = dame_todos_ids_grupos_actuadores_usuario_actual();
                if (in_array($id_grupo_actuadores_accion, $ids_grupos_actuadores_usuario_actual) == false)
                {
                    $log->warn("[".$id_usuario."] Grupo de actuadores no visible por el usuario actual (id: '".$id_grupo_actuadores_accion."')");
                    $parametros_accion_correctos = false;
                }
            }

            // Se comprueban las tarifas
            if ($ids_tarifas_accion !== NULL)
            {
                $ids_sensores_usuario_actual = dame_todos_ids_sensores_usuario_actual();
                $ids_tarifas_usuario_actual = Tarifa::dame_ids_tarifas_usuario_actual(
                    $medicion_accion,
                    $ids_sensores_usuario_actual);
                foreach ($ids_tarifas_usuario_actual as $id_tarifa_accion)
                {
                    if ($id_tarifa_accion !== NULL)
                    {
                        if (in_array($id_tarifa_accion, $ids_tarifas_usuario_actual) == false)
                        {
                            $log->warn("[".$id_usuario."] Tarifa no visible por el usuario actual (id: '".$id_tarifa_accion."')");
                            $parametros_accion_correctos = false;
                        }
                    }
                }
            }

            // Se comprueban los grupos de tarifas
            if ($ids_grupos_tarifas_accion !== NULL)
            {
                $ids_sensores_usuario_actual = dame_todos_ids_sensores_usuario_actual();
                $ids_grupos_tarifas_usuario_actual = GrupoTarifas::dame_ids_grupos_tarifas_usuario_actual(
                    $medicion_accion,
                    $ids_sensores_usuario_actual);
                foreach ($ids_grupos_tarifas_accion as $id_grupo_tarifas_accion)
                {
                    if ($id_grupo_tarifas_accion !== NULL)
                    {
                        if (in_array($id_grupo_tarifas_accion, $ids_grupos_tarifas_usuario_actual) == false)
                        {
                            $log->warn("[".$id_usuario."] Grupo de tarifas no visible por el usuario actual (id: '".$id_grupo_tarifas_accion."')");
                            $parametros_accion_correctos = false;
                        }
                    }
                }
            }

            // Se comprueba el proyecto
            if ($id_proyecto_accion !== NULL)
            {
                $ids_sensores_usuario_actual = dame_todos_ids_sensores_usuario_actual();
                $ids_proyecto_usuario_actual = Proyecto::dame_ids_proyectos_usuario_actual($ids_sensores_usuario_actual);
                if (in_array($id_proyecto_accion, $ids_proyecto_usuario_actual) == false)
                {
                    $log->warn("[".$id_usuario."] Proyecto no visible por el usuario actual (id: '".$id_proyecto_accion."')");
                    $parametros_accion_correctos = false;
                }
            }

            // Se comprueban las líneas base
            if ($ids_lineas_base_accion !== NULL)
            {
                $ids_sensores_usuario_actual = dame_todos_ids_sensores_usuario_actual();
                $ids_lineas_base_usuario_actual = LineaBase::dame_ids_lineas_base_usuario_actual($ids_sensores_usuario_actual);
                foreach ($ids_lineas_base_accion as $id_linea_base_accion)
                {
                    if ($id_linea_base_accion !== NULL)
                    {
                        if (in_array($id_linea_base_accion, $ids_lineas_base_usuario_actual) == false)
                        {
                            $log->warn("[".$id_usuario."] Línea base no visible por el usuario actual (id: '".$id_linea_base_accion."')");
                            $parametros_accion_correctos = false;
                        }
                    }
                }
            }

            // Se devuelve el resultado de la comprobación
            return ($parametros_accion_correctos);
        }

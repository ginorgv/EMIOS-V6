                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de lectura de sensores");
                    $accion_correcta = false;
                }
            }
            if ($permiso_exportacion_sensores == true)
            {
                if (NodoSensor::dame_exportacion_sensores() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de exportacion de sensores");
                    $accion_correcta = false;
                }
            }
            if ($permiso_envio_valores_manuales_sensores == true)
            {
                if (NodoSensor::dame_envio_valores_manuales_sensores() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de envío de valores manuales de sensores");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_eventos == true)
            {
                if (Evento::dame_administracion_eventos() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de eventos");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_actuadores == true)
            {
                if (NodoActuador::dame_administracion_actuadores() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de actuadores");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_comentarios_actuadores == true)
            {
                if (NodoActuador::dame_administracion_comentarios_actuadores() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de comentarios de actuadores");
                    $accion_correcta = false;
                }
            }
            if ($permiso_envio_acciones_actuadores == true)
            {
                if (NodoActuador::dame_envio_acciones_actuadores() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de envío de acciones a actuadores");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_programaciones == true)
            {
                if (Programacion::dame_administracion_programaciones() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de programaciones");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_reglas == true)
            {
                if (Regla::dame_administracion_reglas() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de reglas");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_validaciones_facturas == true)
            {
                if (dame_administracion_validaciones_facturas() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de validaciones de facturas");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_tarifas == true)
            {
                if (Tarifa::dame_administracion_tarifas() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de tarifas");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_proyectos == true)
            {
                if (Proyecto::dame_administracion_proyectos() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de proyectos");
                    $accion_correcta = false;
                }
            }

            // Se devuelve el resultado de la comprobación
            return ($accion_correcta);
        }



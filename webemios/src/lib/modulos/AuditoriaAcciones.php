                case ACCION_MOSTRAR_VENTANA_ASIGNACION_LOCALIZACION_NODOS:
                case ACCION_ASIGNAR_LOCALIZACION_NODOS:
                case ACCION_MOSTRAR_VENTANA_ASIGNACION_GRUPO_NODOS:

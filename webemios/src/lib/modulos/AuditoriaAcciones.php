            if ($permiso_administracion_localizaciones == true)
            {
                if (Localizacion::dame_administracion_localizaciones() == false)
                {
                    $log->warn("[".$id_usuario."] El usuario no tiene permiso de administración de localizaciones");
                    $accion_correcta = false;
                }
            }
            if ($permiso_administracion_instalaciones == true)
            {
                if (Instalacion::dame_administracion_instalaciones() == false)

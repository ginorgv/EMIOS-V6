        // Función que comprueba si se puede ejecutar una acción (modulos, perfiles y parámetros de la acción)
        // y añade la acción al log
        static function comprobar_registrar_accion($accion, $parametros_accion)
        {
            // Se comprueba si el usuario que ejecuta la acción es un usuario interno (de Servidor EMIOS)
            if (!isset($_SESSION["usuario_interno"]))
            {
                $usuario_interno = False;
                $id_usuario = $_SESSION["id_usuario"];
            }
            else
            {
                $usuario_interno = True;
                $id_usuario = "{INTERNO} ".$_SESSION["usuario_interno"];
            }

            // Se guarda la acción en el log
            $log = dame_log();
            $mensaje_log = "{".VERSION_WEB." - ".NUMERO_LIBERACION_WEB." (".FECHA_LIBERACION_WEB.")} ";
            switch ($accion)
            {
                case ACCION_GUARDAR_IMAGEN_SERVIDOR_DATOS_IMAGEN:
                {
                    $mensaje_log .= "[".$id_usuario."] ".
                        "Acción: '".$accion."', ".
                        "parámetros: {".
                        '"nombre_fichero":"'.$parametros_accion["nombre_fichero"].'"}';
                    break;
                }
                case ACCION_GUARDAR_VALORES_CSV:
                {
                    $mensaje_log .= "[".$id_usuario."] ".
                        "Acción: '".$accion."', ".
                        "parámetros: {".
                        '"nombre_fichero_csv":"'.$parametros_accion["nombre_fichero_csv"].'"}';
                    break;
                }
                default:
                {
                    $mensaje_log .= "[".$id_usuario."] ".
                        "Acción: '".$accion."', ".
                        "parámetros: ".json_encode($parametros_accion);
                    break;
                }
            }
            $log->info($mensaje_log);
            if ($usuario_interno == True)
            {
                return;
            }

            // Comprobación de sesión correcta
            $respuesta_sesion_correcta = dame_sesion_correcta();
            if ($respuesta_sesion_correcta["sesion_correcta"] == false)
            {
                throw new Exception(
                    "Sesión incorrecta (usuario: ".$id_usuario.", acción: '".$accion."')",
                    CODIGO_EXCEPCION_SESION_INCORRECTA);
            }

            // Se comprueba la acción (para comprobar que se corresponden con los permisos actuales del usuario)
            $accion_correcta = AuditoriaAcciones::dame_accion_correcta(
                $id_usuario,
                $accion,
                $parametros_accion);
            if ($accion_correcta == false)
            {
                throw new Exception("Acción incorrecta (usuario: ".$id_usuario.", acción: '".$accion."')");
            }

            // Se comprueban los parámetros de la acción (para comprobar que se corresponden con los permisos actuales del usuario)
            $parametros_accion_correctos = AuditoriaAcciones::dame_parametros_accion_correctos(
                $id_usuario,
                $accion,
                $parametros_accion);
            if ($parametros_accion_correctos == false)
            {
                throw new Exception("Parámetros de la acción incorrectos (usuario: ".$id_usuario.", acción: '".$accion."')");
            }
        }



        }


        static function dame_descripcion_tipo_seleccion_elemento($tipo_seleccion_elemento)
        {
            switch ($tipo_seleccion_elemento)
            {
                case TIPO_SELECCION_ELEMENTO_PLANTILLA_INFORME_FIJO:
                {
                    $descripcion_tipo_seleccion_elemento = "Fija";
                    break;
                }
                case TIPO_SELECCION_ELEMENTO_PLANTILLA_INFORME_CONFIGURABLE:
                {
                    $descripcion_tipo_seleccion_elemento = "Configurable";
                    break;
                }
                default:
                {
                    $descripcion_tipo_seleccion_elemento = "Desconocido";
                    break;
                }
            }
            $idiomas = new Idiomas();
            return ($idiomas->_($descripcion_tipo_seleccion_elemento));
        }



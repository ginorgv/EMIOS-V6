        // Devuelve la tabla de elementos
        static function dame_tabla_elementos($id_plantilla_informe)
        {
            $idiomas = new Idiomas();
            $bd_red = BaseDatosRed::dame_base_datos();

            // Se recuperan los elementos
            $consulta = ElementoPlantillaInforme::dame_consulta_elementos($id_plantilla_informe);
            $res = $bd_red->ejecuta_consulta($consulta);
            if ($res == false)
            {
                throw new Exception("Error en la consulta: '".$consulta."'");
            }
            $numero_elementos = $res->dame_numero_filas();

            // Se recupera el número de elementos computables
            $numero_elementos_no_computables = ElementoPlantillaInforme::dame_numero_elementos_no_computables($id_plantilla_informe);
            $numero_elementos_computables = ($numero_elementos - $numero_elementos_no_computables);

            // Opciones de la tabla
            $opciones = array();
            $administracion_plantillas_informes = PlantillaInforme::dame_administracion_plantillas_informes();
            $permitir_adicion_elementos = false;
            if ($administracion_plantillas_informes == true)
            {
                $boton_eliminar_elementos = "<i id='eliminar_elementos__".$id_plantilla_informe."' class='icon-remove color-blanco boton_personal_mostrar_ventana_eliminar_elementos_plantilla_informe boton-tabla-datos'></i>";
                array_push($opciones, $boton_eliminar_elementos);
                $boton_modificar_posiciones_elementos = "<i id='modifica_posiciones_elementos__".$id_plantilla_informe."' class='icon-pencil color-blanco boton_personal_mostrar_ventana_modificar_posiciones_elementos_plantilla_informe boton-tabla-datos'></i>";
                array_push($opciones, $boton_modificar_posiciones_elementos);
                if ($numero_elementos_computables < NUMERO_MAXIMO_ELEMENTOS_PLANTILLAS_INFORMES)
                {
                    $permitir_adicion_elementos = true;
                    $boton_anyadir_elemento = "<i id='anyade_modifica_elemento__".$id_plantilla_informe."' class='icon-plus color-blanco boton_personal_mostrar_ventana_anyadir_modificar_elemento_plantilla_informe boton-tabla-datos'></i>";
                    array_push($opciones, $boton_anyadir_elemento);
                }
            }
            $boton_actualizar_tabla_elementos = "<i id='actualiza_tabla_elementos__".$id_plantilla_informe."' class='icon-refresh color-blanco boton_personal_actualizar_tabla_elementos_plantilla_informe boton-tabla-datos'></i>";
            array_push($opciones, $boton_actualizar_tabla_elementos);

            // Se crea la tabla
            $params_tabla = array(
                "opciones" => $opciones,
                "numero_columnas" => NUMERO_COLUMNAS_TABLA_ELEMENTOS_PLANTILLAS_INFORMES,
                "anchuras_columnas" => unserialize(ANCHURAS_COLUMNAS_TABLA_ELEMENTOS_PLANTILLAS_INFORMES),
                "generar_valores_xml" => true
            );
            $tabla = new TablaDatos(
                "tabla-elementos-plantillas-informes",
                $idiomas->_("Elementos"),
                TIPO_TABLA_DATOS_LISTA,
                $params_tabla
            );
            $cabecera = ElementoPlantillaInforme::dame_cabecera_tabla();
            $tabla->anyade_cabecera("", $cabecera);

            // Se añade cada uno de los elementos a la tabla y el pie de tabla
            $numero_pagina = 1;
            while ($fila = $res->dame_siguiente_fila())
            {
                switch ($fila["tipo"])
                {
                    case TIPO_ELEMENTO_PLANTILLA_INFORME_PORTADA:
                    {
                        if ($numero_pagina > 1)
                        {
                            $numero_pagina += 1;
                        }
                        break;
                    }
                }
                $elemento_plantilla_informe = new ElementoPlantillaInforme($fila);
                $params_fila = array(
                    "opciones" => $elemento_plantilla_informe->dame_opciones_tabla($permitir_adicion_elementos),
                );
                $tabla->anyade_fila(
                    "datosElementoPlantillaInforme__".$id_plantilla_informe."__".$fila['id'],
                    $elemento_plantilla_informe->dame_datos_tabla($numero_pagina),
                    $params_fila
                );
                switch ($fila["tipo"])
                {
                    case TIPO_ELEMENTO_PLANTILLA_INFORME_SALTO_PAGINA:
                    case TIPO_ELEMENTO_PLANTILLA_INFORME_PORTADA:
                    {
                        $numero_pagina += 1;
                        break;
                    }
                }
            }
            $tabla->anyade_pie($idiomas->_("Elementos").": ".$numero_elementos_computables.
                " (".$idiomas->_("máximo").": ".NUMERO_MAXIMO_ELEMENTOS_PLANTILLAS_INFORMES.")");

            return ($tabla->dame_tabla(false));
        }



        function dame_datos_tabla($numero_pagina)
		{
            // Iconos de datos erróneos
            $icono_fila_con_errores = dame_icono_fila_con_errores();
            $icono_dato_erroneo = dame_icono_dato_erroneo();

            // Inicialización de datos de la tabla
            $nombre = $icono_dato_erroneo;
            $descripcion_tipo = $icono_dato_erroneo;

            // Se recuperan los datos de la tabla
            try
            {
                // Nombre y descripción de tipo
                $nombre = htmlspecialchars($this->params["nombre"], ENT_QUOTES);
                $descripcion_tipo = ElementoPlantillaInforme::dame_descripcion_tipo_elemento($this->params["tipo"]);

                // Modificación de estilos en saltos de página y de línea
                switch ($this->params["tipo"])
                {
                    case TIPO_ELEMENTO_PLANTILLA_INFORME_SALTO_PAGINA:
                    case TIPO_ELEMENTO_PLANTILLA_INFORME_SALTO_LINEA:
                    {
                        $numero_pagina = "<font class='color-gris-muy-claro'>".$numero_pagina."</font>";
                        $nombre = "<font class='color-gris-muy-claro'>".htmlspecialchars($nombre, ENT_QUOTES)."</font>";
                        $descripcion_tipo = "<font class='color-gris-muy-claro'>".$descripcion_tipo."</font>";
                        break;
                    }
                }
            }
            catch (Exception $e)
            {
                // Se añade información de la excepción en el log
                $log = dame_log();
                $log->error("[".$_SESSION["id_usuario"]."] "."Excepción capturada: ", $e);

                // Se añade icono de error en el número de página
                $numero_pagina = "[".$icono_fila_con_errores."] ".$numero_pagina;
            }

            // Se devuelven los datos de la tabla
            return (array(
                $numero_pagina,
                $nombre,
                $descripcion_tipo
            ));
		}



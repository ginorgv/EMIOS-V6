        static function dame_tipos_seleccion_elemento()
        {
            $tipos_seleccion = array();
            array_push($tipos_seleccion, TIPO_SELECCION_ELEMENTO_PLANTILLA_INFORME_FIJO);
            array_push($tipos_seleccion, TIPO_SELECCION_ELEMENTO_PLANTILLA_INFORME_CONFIGURABLE);

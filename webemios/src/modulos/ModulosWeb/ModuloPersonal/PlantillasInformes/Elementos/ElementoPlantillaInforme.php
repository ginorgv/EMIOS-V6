        static function dame_nombres_valores_parametros_tipo_elemento(
            $tipo_elemento,
            $cadena_parametros_tipo_elemento,
            $cadena_parametros_tipo_json_elemento)
        {
            // Se recuperan los parámetros de tipo del elemento
            $parametros_tipo_elemento = explode(SEPARADOR_PARAMETROS_COMPUESTOS, $cadena_parametros_tipo_elemento);
            if ($cadena_parametros_tipo_json_elemento != "")
            {
                $parametros_tipo_json_elemento = json_decode_caracteres_especiales($cadena_parametros_tipo_json_elemento);
            }
            $nombres_valores_parametros_tipo_elemento = array();
            switch ($tipo_elemento)
            {
                case TIPO_ELEMENTO_PLANTILLA_INFORME_SALTO_PAGINA:
                case TIPO_ELEMENTO_PLANTILLA_INFORME_SALTO_LINEA:
                {
                    break;
                }
                case TIPO_ELEMENTO_PLANTILLA_INFORME_PORTADA:

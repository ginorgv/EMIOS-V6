establece_eventos_secciones_localizaciones_instalaciones = function() {
    // Desactivación de eventos anteriores
    $("#id_localizacion_localizaciones_seleccion_imagen_instalacion").off();
    $("#id_localizacion_localizaciones_seleccion_topologia_instalacion").off();

    // Redibujado de topología de localización
    $('#pestanyas-instalaciones-informes-localizaciones').off('shown.bs.tab');
    $('#pestanyas-instalaciones-informes-localizaciones').on('shown.bs.tab', 'a[data-toggle="tab"]', function (e) {
        var href_pestanya_activa = $('#pestanyas-instalaciones-informes-localizaciones .active > a').attr('href');
        var id_pestanya_activa = href_pestanya_activa.replace("#tab-", "");
        switch (id_pestanya_activa) {
            case "mapa-instalaciones": {
                boton_localizaciones_muestra_mapa_instalaciones_localizacion_seleccionada();
                break;
            }
            case "imagen-instalacion": {
                boton_localizaciones_muestra_imagen_instalacion_instalacion_seleccionada();
                break;
            }
        }
    });

    // Habilitación de instalación de localización
    funcion_habilita_instalacion_localizacion = function(id_controles) {
        // Se deshabilita si sólo hay un valor para elegir
        var numero_instalaciones = $("select#id_instalacion_localizaciones_" + id_controles + " option").length;
        if (numero_instalaciones <= 1) {
            $("#id_instalacion_localizaciones_" + id_controles).attr('disabled', true).trigger("chosen:updated");
        }
        else {
            $("#id_instalacion_localizaciones_" + id_controles).removeAttr('disabled').trigger("chosen:updated");
        }
    };
    var funcion_habilita_instalacion_localizacion_seleccion_imagen_instalacion = function() {
        funcion_habilita_instalacion_localizacion("seleccion_imagen_instalacion");
    };
    $("#id_localizacion_localizaciones_seleccion_imagen_instalacion").show(funcion_habilita_instalacion_localizacion_seleccion_imagen_instalacion);
    var funcion_habilita_instalacion_localizacion_seleccion_topologia_instalacion = function() {
        funcion_habilita_instalacion_localizacion("seleccion_topologia_instalacion");
    };
    $("#id_localizacion_localizaciones_seleccion_topologia_instalacion").show(funcion_habilita_instalacion_localizacion_seleccion_topologia_instalacion);

    // Recarga de los las instalaciones de una localización
    var funcion_recarga_instalaciones_localizacion = function(id_controles, opciones_extra) {
        var id_localizacion = $("#id_localizacion_localizaciones_" + id_controles).val();
        var id_instalacion = $("#id_instalacion_localizaciones_" + id_controles).val();
        $.post("./src/lib/modulos/localizaciones/dame_lista_instalaciones_localizacion.php", {
            id_localizacion: id_localizacion,
            id_instalacion: id_instalacion,
            opciones_extra: opciones_extra
        },
        function (data, status) {
            var resultado = dame_resultado_ejecucion_script_php_json(data);
            if (resultado == null) {
                return;
            }

            $("#id_instalacion_localizaciones_" + id_controles).html(resultado.html);
            $("#id_instalacion_localizaciones_" + id_controles).trigger("chosen:updated");

            funcion_habilita_instalacion_localizacion(id_controles);
        });
    };
    var funcion_recarga_instalaciones_localizacion_seleccion_imagen_instalacion = function() {
        funcion_recarga_instalaciones_localizacion("seleccion_imagen_instalacion", OPCIONES_EXTRA_LISTA_INSTALACIONES_CON_IMAGEN_NINGUNA);
    };
    $("#id_localizacion_localizaciones_seleccion_imagen_instalacion").change(funcion_recarga_instalaciones_localizacion_seleccion_imagen_instalacion);
    var funcion_recarga_instalaciones_localizacion_seleccion_topologia_instalacion = function() {
        funcion_recarga_instalaciones_localizacion("seleccion_topologia_instalacion", OPCIONES_EXTRA_LISTA_INSTALACIONES_NINGUNA);
    };
    $("#id_localizacion_localizaciones_seleccion_topologia_instalacion").change(funcion_recarga_instalaciones_localizacion_seleccion_topologia_instalacion);
};



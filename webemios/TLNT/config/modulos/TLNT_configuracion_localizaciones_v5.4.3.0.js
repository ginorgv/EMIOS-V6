establece_eventos_ventanas_modales_localizaciones_localizaciones = function() {
    // Desactivación de eventos anteriores
    $("#descripcion_localizacion").off();
    $("#mapa_personalizado_localizacion").off();
    $("#tipo_mapa_localizacion").off();
    $("#tipo_mapa_localizacion").off();

    // Contador de caracteres de descripción de localización
    $("#descripcion_localizacion").on('input', TLNT.Navegacion.actualiza_contador_caracteres_textarea);

    // Ventana de administración de localización:
    // - Selección de fichero de imagen de mapa local
    $("#fichero_imagen_mapa_localizacion_text").show(function() {
        $('#fichero_imagen_mapa_localizacion_file').hide();
    });
    $('#fichero_imagen_mapa_localizacion_file').change(function() {
        var fichero = $(this).val().split('\\').pop();
        $('#fichero_imagen_mapa_localizacion_text').val(fichero);
    });
    $('#boton_anyadir_modificar_localizacion_seleccionar_fichero_imagen_mapa').click(function() {
        $('#fichero_imagen_mapa_localizacion_file').click();
    });

    // Habilita y muestra los controles dependientes del tipo de mapa de localización
    var funcion_habilita_muestra_controles_tipo_mapa_localizacion = function() {
        var mapa_personalizado = parseInt($("#mapa_personalizado_localizacion").val());
        if (mapa_personalizado == VALOR_SI) {
            var tipo_mapa = $("#tipo_mapa_localizacion").val();
            switch (tipo_mapa) {
                case TIPO_MAPA_INTERNET: {
                    $("#nombre_mapa_localizacion").removeClass('TLNT_input_mandatory');
                    $("#control_fichero_imagen_mapa_localizacion").hide();
                    $("#factor_reduccion_imagen_mapa_local_localizacion").removeClass('TLNT_input_mandatory');
                    $("#control_factor_reduccion_imagen_mapa_local_localizacion").hide();
                    break;
                }
                case TIPO_MAPA_LOCAL: {
                    $("#nombre_mapa_localizacion").addClass('TLNT_input_mandatory');
                    $("#control_fichero_imagen_mapa_localizacion").show();
                    $("#factor_reduccion_imagen_mapa_local_localizacion").addClass('TLNT_input_mandatory');
                    $("#control_factor_reduccion_imagen_mapa_local_localizacion").show();
                    break;
                }
            }
        }
    };
    $("#tipo_mapa_localizacion").show(funcion_habilita_muestra_controles_tipo_mapa_localizacion);
    $("#tipo_mapa_localizacion").change(funcion_habilita_muestra_controles_tipo_mapa_localizacion);

    // Habilita y muestra los controles dependientes de si hay mapa personalizado
    var funcion_habilita_muestra_controles_mapa_personalizado_localizacion = function() {
        var mapa_personalizado = parseInt($("#mapa_personalizado_localizacion").val());
        switch (mapa_personalizado) {
            case VALOR_NO: {
                $("#control_tipo_mapa_localizacion").hide();
                $("#control_nombre_mapa_localizacion").hide();
                $("#nombre_mapa_localizacion").removeClass('TLNT_input_mandatory');
                $("#control_fichero_imagen_mapa_localizacion").hide();
                $("#control_factor_reduccion_imagen_mapa_local_localizacion").hide();
                $("#control_etiquetas_mapa_localizacion").hide();

                // Se oculta la pestaña del mapa
                $("#titulo-tab-mapa").hide();
                break;
            }
            case VALOR_SI: {
                $("#control_tipo_mapa_localizacion").show();
                $("#control_nombre_mapa_localizacion").show();
                $("#control_etiquetas_mapa_localizacion").show();

                // Se muestran los controles según el tipo de mapa de la localización
                funcion_habilita_muestra_controles_tipo_mapa_localizacion();

                // Se muestra la pestaña del mapa (sólo si se está modificando y había mapa personalizado)
                var anyadir_localizacion = $("#parametros_ventana_anyadir_modificar_localizacion").attr("anyadir_localizacion");
                var mapa_personalizado = parseInt($("#parametros_ventana_anyadir_modificar_localizacion").attr("mapa_personalizado"));
                if ((anyadir_localizacion == false) && (mapa_personalizado == VALOR_SI)){
                    $("#titulo-tab-mapa").show();
                }
                break;
            }
        }
    };
    $("#mapa_personalizado_localizacion").show(funcion_habilita_muestra_controles_mapa_personalizado_localizacion);
    $("#mapa_personalizado_localizacion").change(funcion_habilita_muestra_controles_mapa_personalizado_localizacion);
};


